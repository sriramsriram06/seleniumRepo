package LearnAutomation1;

public class learnForLoop {
	
	 public static void main(String[] args) {

	        int num = 10;  // declare a number to add with i

	        for (int i = 5; i < 10; i++) {

	            System.out.println(num + " / " + i + " = " + (num + i));

	        }
	    }

	}


